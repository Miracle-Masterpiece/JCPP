#include <cstdint>

/**
 * Абстрактный базовый класс для потокового сжатия данных.
 * Класс предоставляет интерфейс для пошагового (chunk-by-chunk) сжатия данных.
 * Объекты класса не могут быть скопированы, но поддерживают семантику перемещения.
 */
class compressor {
protected:
    // Запрет копирования
    compressor(const compressor&) = delete;
    compressor& operator=(const compressor&) = delete;

    /**
     * Указатель на текущий буфер с несжатыми входными данными.
     */
    const char* input;
    
    /**
     * Размер входных данных в буфере input (в байтах).
     */
    std::size_t input_size;

    /**
     * Флаг завершения процесса сжатия.
     * 
     * Становится true, когда все входные данные обработаны и внутренние
     * буферы алгоритма (флаг завершения потока) полностью сброшены в dst.
     */
    bool finished;

protected:
    /**
     * Возвращает указатель на текущую позицию во входном буфере.
     * @return const char* Указатель на данные.
     */
    const char* get_input() const;
    
    /**
     * Полный размер входного буфера #input в байтах.
     */
    std::size_t get_size() const;

    /**
     * Переводит компрессор в состояние "завершено".
     * 
     * Метод должен вызываться наследником, когда алгоритм сжатия
     * полностью обработал вход и записал все финальные маркеры (EOF архива).
     */
    void finish();

public:
    /**
     * Конструктор по умолчанию.
     * @details Инициализирует компрессор в исходное состояние (вход пуст, finished = false).
     */
    compressor();
    
    /**
     * Конструктор перемещения.
     */
    compressor(compressor&&);
    
    /**
     * Оператор перемещающего присваивания.
     */
    compressor& operator=(compressor&&);
    
    /**
     * Устанавливает новый буфер входных данных для сжатия.
     * @param data
     *      Указатель на начало буфера с данными.
     * 
     * @param len
     *      Размер буфера в байтах.
     * 
     * @note 
     *      Метод вызывается пользователем перед вызовом compress(), когда старая порция
     *      данных была полностью обработана (get_size() вернул 0).
     */
    virtual void set_input(const char* data, std::size_t len);
    
    /**
     * Проверяет, завершен ли процесс сжатия.
     * 
     * @return 
     *      true Если все данные сжаты и поток финализирован, иначе false.
     */
    bool is_finished() const;

    /**
     * Основной метод сжатия. Обрабатывает входные данные и пишет результат в dst.
     * 
     * @param dst
     *      Выходной буфер для записи сжатых данных.
     * 
     * @param sz
     *      Максимально доступный размер буфера dst в байтах.
     * 
     * @return 
     *      Количество байт, фактически записанных в буфер dst.
     * 
     * @details
     *      Метод возвращает 0, если процесс сжатия полностью завершен (is_finished() == true).
     */
    virtual std::size_t compress(char dst[], std::size_t sz) = 0;

    /**
     * 
     */
    virtual ~compressor();
};

#endif /* AAC7C627_E31E_4105_BCFC_83270DB4A54B */
